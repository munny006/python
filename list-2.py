# function
a = ["c", "c++", "java", "oop"]

# length function
print(len(a))
# append function means add
a.append("toc")
print(a)

# insert
a.insert(2, "php")
print(a)
# remove function
a.remove("oop")
print(a)

# sorting function
a.sort()
print(a)
# reverse function
a.reverse()
print(a)

# pop
a.pop()
print(a)
# clear function
a.clear()
print(a)

# copy function
m = [20, 10, 4, 5]
b = m.copy()
print(b)

# position function
s = [20, 10, 4, 5,2,3,4]
an = s.index(3)
print(an)

#count function
s = [20, 10, 4, 5,2,3,4]
an = s.count(4)
print(an)
